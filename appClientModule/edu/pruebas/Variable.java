package edu.pruebas;

public class Variable {
	
	private Integer x,y;
	
	public Variable(Integer x, Integer y) {
		super();
		this.x = x;
		this.y = y;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}
	
	

}
